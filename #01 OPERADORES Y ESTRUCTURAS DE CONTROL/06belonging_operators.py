"""
With this kind of operators,
we are able to validate if an element is inside a collection.
"""

example1 = "G" in "Guillermo"
print(example1)

example1 = "G" not in "Guillermo"
print(example1)

example2 = [1,2,4,5] in [[1,2,4,5]]
print(example2)

example2 = [1,2,4,5] not in [[1,2,4,5]]
print(example2)